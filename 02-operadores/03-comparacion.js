
let a = 10
console.log(a > 5); // si a es mayor a 5
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);


console.log(a == 10); // si es exactamente a 10
console.log(a != 10); // si es distinto a 10

//se utilizan mas estos de === y !==
console.log(a === '10'); // con el === compara el tipo de dato si string o numero
console.log(a !== '10');





